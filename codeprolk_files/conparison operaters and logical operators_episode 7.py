#camparison operators
#equal operator(==)
x=100
y=200
print(x==y)

t=125
s=125
print(t==s)

#not equal operator
a=120
b=120
print(a!=b)

c=190
d=200
print(c!=d)

#greater than operator
e=200
f=300
print(e>f)

#less than operator
g=250
h=350
print(g<h)

#greater than or equal operator
i=145
j=145
print(i>=j)

#less than or equal operator
k=135
l=135
print(k<=l)

#checking strings whether they are simple or capital
u="vishwa praveen"
print(u.islower())
o="PRAVEEN"
print(o.isupper())

#logical operators
#and operator
print(3>1 and 4<1)

#or operator
print(100<200 or 200>400)

#not operator
print(not 100>1000)